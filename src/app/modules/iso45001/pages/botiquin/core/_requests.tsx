import axios from 'axios'
const BASE_URL = import.meta.env.VITE_APP_API_URL

export const GET_BOTIQUINES_URL = `${BASE_URL}/api/botiquines`
export const GET_BOTIQUIN_URL = `${BASE_URL}/api/botiquin`
export const DELETE_BOTIQUIN_URL = `${BASE_URL}/api/trabajador`

export function getBotiquines() {
    return axios.get(GET_BOTIQUINES_URL)
}

export function getBotiquinById(id: string) {
    return axios.get(`${GET_BOTIQUIN_URL}/${id}`)
}

export function deleteBotiquinService(id: string) {
    return axios.delete(`${DELETE_BOTIQUIN_URL}/${id}`)
}